var Event = function(id, name, description, targetGroup, contributionsDescription, location, times, maximalAmountOfGuests, guests){
    this.id = id;
    this.name = name;
    this.description = description;
    this.targetGroup = targetGroup;
    this.contributionsDescription = contributionsDescription;
    this.location = location;
    this.times = times;
    this.maximalAmountOfGuests = maximalAmountOfGuests;
    this.guests = guests;
};